package com.github.jordane_quincy;

public class Document {

	private int idUtilisateur;
	private int dureeImpression;

	public int getIdUtilisateur() {
		return idUtilisateur;
	}

	public void setIdUtilisateur(int idUtilisateur) {
		this.idUtilisateur = idUtilisateur;
	}

	public int getDureeImpression() {
		return dureeImpression;
	}

	public void setDureeImpression(int dureeImpression) {
		this.dureeImpression = dureeImpression;
	}

	public Document(int idUtilisateur, int dureeImpression) {
		this.idUtilisateur = idUtilisateur;
		this.dureeImpression = dureeImpression;
	}

}
